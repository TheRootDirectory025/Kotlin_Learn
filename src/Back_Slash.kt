fun main(){
    val name = "Mohsen Bagheri."
    // \b is Back-Space
    println("$name\b")
    // \n moves to new line
    println("$name\nnew line")
    // \t print a TAB
    println("$name\t test text")
    // printing \
    println("$name \\ end")
    // printing " or '
    println("$name \" and \' end")

}